CREATE OR REPLACE PROCEDURE DISP_EMP_DETAILS_2251
(
IEMPNO IN EMP.EMPNO%TYPE,
SGRADE OUT SALGRADE.GRADE%TYPE,
SSALARY OUT EMP.SAL%TYPE)
AS
BEGIN
SELECT DISTINCT GRADE, SAL INTO SGRADE, SSALARY FROM EMP INNER JOIN SALGRADE ON (EMP.SAL BETWEEN SALGRADE.LOSAL AND SALGRADE.HISAL) WHERE EMPNO=IEMPNO;
END;


SET SERVEROUTPUT ON;
DECLARE
SGRADE SALGRADE.GRADE%TYPE;
SSALARY EMP.SAL%TYPE;
BEGIN
DISP_EMP_DETAILS_2251(7902,SGRADE,SSALARY);
DBMS_OUTPUT.PUT_LINE('GRADE:'||SGRADE);
DBMS_OUTPUT.PUT_LINE('SALARY:'||SSALARY);
END; 


