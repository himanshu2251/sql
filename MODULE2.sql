CREATE OR REPLACE PROCEDURE DISPLAY_RECORDS_2251(P_JOB EMP.JOB%TYPE)
AS 
BEGIN

FOR REC IN(
SELECT EMPNO,SAL,DEPTNO,JOB FROM EMP WHERE JOB=P_JOB)
LOOP
DBMS_OUTPUT.PUT('EMPNO:' || REC.EMPNO);
DBMS_OUTPUT.PUT('SALARY:' || REC.SAL);
DBMS_OUTPUT.PUT('DEPTNO:' || REC.DEPTNO);
DBMS_OUTPUT.PUT('JOB:' || REC.JOB);
DBMS_OUTPUT.PUT_LINE('');
END LOOP;
END;

SET SERVEROUTPUT ON;
BEGIN
hr.DISPLAY_RECORDS_2251('CLERK');
END;

