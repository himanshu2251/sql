CREATE OR REPLACE FUNCTION GET_EMP_ANNUAL (IEMPNO EMP.EMPNO%TYPE)
RETURN NUMBER IS
ANNUAL_SALARY NUMBER(10,0):=0;
BEGIN
SELECT SAL*12 INTO ANNUAL_SALARY
FROM EMP
WHERE EMPNO=IEMPNO;
RETURN ANNUAL_SALARY;
END;

SET SERVEROUTPUT ON;
DECLARE
C NUMBER(10,0);
BEGIN
C:= GET_EMP_ANNUAL(7900);
DBMS_OUTPUT.PUT_LINE('ANNUAL SALARY:' || C);
END;

